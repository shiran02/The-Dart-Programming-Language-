void main(){
  //For loop .......

  var num = 5;
  for(int i =num ; i>= 1;i--){
    print(i);
  }

  //for in loop .....

  var names = ["shiran","uditha","rashmika"];
  for(var name in names){
    print(name);
  }

  var cars =["bwm","v8","audi"];

  for(var car in cars){
    print(car);
  }


  //While loop .....

  var count = 0;
  while(count<10){
    print(count);
    count++;
  }

}